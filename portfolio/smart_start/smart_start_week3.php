                <h6>Wat is lean?</h6>
                <hr>
                <p>Lean is een productiesysteem dat gericht is op het verbeteren van de organisatie door ‘continuous
                improvements’ in te voeren. Het gaat hierbij om de principes meer doen met minder, meeste rendement en
                laagste kosten.

                Belangrijke vragen in deze casus zijn: Kunnen we het zelfde aantal blikjes produceren met minder machines?
                Kunnen we andere processen op efficiëntere manieren doen? Kunnen we ‘slimme’ machines inzetten die sneller
                of efficiënter kunnen produceren? Kunnen we kunstmatige intelligentie gebruiken om het systeem zelf te leren
                wat de snelste manier van produceren is?

                Dit kan bijvoorbeeld gedaan worden met Big data. Als uit heel veel data blijkt dat er een snellere manier is
                om te produceren, kan het bedrijf deze manier ook gebruiken.
                </p>
                <h6>Wat is agile?</h6>
                <hr>
                <p>Agile is het vermogen van een bedrijf om te (snel) te veranderen/verbeteren. Hierbij kan worden gedacht aan
                    slimme machines, verandering in productie en kostenvermindering.
                    Het lijkt erg op Lean.

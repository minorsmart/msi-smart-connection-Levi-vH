                    meer te weten te komen over het thema Smart Industry.</p>
                <h5>Wat is Smart Industry?</h5>
                <hr>
                <p> Smart industry is de verzamelnaam voor het automatiseren of verbeteren van (bedrijfs)processen door
                    middel
                    van de nieuwste techniek. Dit kan op veel verschillende vakken gebeuren.
                    Er kan gekeken worden naar het product wat een bedrijf verkoopt, of naar bepaalde processen die in
                    een
                    bedrijf geautomatiseerd kunnen worden.
                    Het doel van smart industry is om ervoor te zorgen dat bijvoorbeeld de waarde van een product
                    stijgt, of dat
                    een bedrijf processen kan optimaliseren. Dit alles zorgt voor meer omzet, winst en innovatie.
                    Ook kan er artificial intellence worden ingezet. Dit kan bijvoorbeeld om onderhoud aan machines te
                    voorspellen. Hierdoor kan een machine langer draaien zonder problemen.
                    De industry kan op veel verschillende manieren ‘smart’ worden gemaakt. Hieronder staat het diagram
                    dat wordt
                    gebruikt om de categorieën te beschrijven die horen bij smart industry.
                    Onder het diagram staan alle verschillende onderdelen uitgelegd.
                </p>
                <ul>
                    <b>
                        <li>Smart products</li>
                    </b>
                    Smart Products zijn in staat om gegevens te verzamelen, op te slaan en kunnen communiceren met hun
                    omgeving.
                    Bijvoorbeeld via het internet. Ze geven informatie over hun identiteit, eigenschappen en
                    geschiedenis.
                    <b>
                        <li>Servitization</li>
                    </b>
                    Servitization(Slimme Diensten) is het proces waarbij dienstverlening een grotere rol gaat spelen bij
                    het
                    business model van bedrijven. Service verandert van kostenpost naar extra omzet. Bij slimme diensten
                    onderhoudt en beheert de leverancier haar eigen software.
                    <b>
                        <li>Digital Factory</li>
                    </b>
                    Bij een digitale fabriek zijn alle informatiestromen intern met elkaar verbonden. Van kantoor,
                    design,
                    productie, logistiek tot onderhoud en beheer aan toe. Hierbij verlopen alle processen automatisch.
                    <b>
                        <li>Connected Factories</li>
                    </b>
                    Bij een digitale keten zijn alle betrokkenen digitaal verbonden, van fabriek tot aan de klant en
                    kunnen ze
                    data uitwisselen volgens internationale standaarden gekoppelde IT-systemen. Dit moet leiden tot
                    minder
                    kosten voor bijvoorbeeld offertes of snellere doorlooptijden van bestellingen.
                    <b>
                        <li>Sustainable Factory</li>
                    </b>
                    Een duurzame fabriek werkt zo zuinig mogelijk met de energie en materiaal de zij verbruiken. Ook
                    zijn de
                    producten die zij maken herbruikbaar.
                    <b>
                        <li>Smart Working</li>
                    </b>
                    Slim werken helpt de technologie om zware, vieze en gevaarlijke klussen lichter, schoner en veiliger
                    te
                    maken, of om complexe handelingen foutloos uit te voeren. Slimmer werken betekent ook leven-lang
                    leren om
                    nieuwe digitale technieken te leren begrijpen en te leren hoe ze worden toegepast.
                    <b>
                        <li>Advanced Manufacturing</li>
                    </b>
                    Slim produceren is foutloos produceren omdat elke productiestap 100% wordt gecontroleerd. Dankzij
                    foutloze
                    producten (zero defect) worden faalkosten geminimaliseerd.
                    <b>
                        <li>Flexible Manufacturing</li>
                    </b>
                    Bij flexibel produceren is een fabriek in staat om steeds andere producten te realiseren met de
                    kortst
                    mogelijke doorlooptijden, van offerte tot levering en van order tot levering. Hierbij kunnen kleine
                    series
                    worden geleverd en enkelstuks.
                </ul>
                <h5>Persoonlijke studie en kruispunten</h5>
                <hr>
                <p>
                    Mijn studie HBO-ICT en mijn specificatie ‘Software Development’ komen op een paar punten overeen met
                    de
                    onderwerpen die horen bij Smart Industry. Hieronder worden alle thema’s behandeld.
                <ul>
                    <b>
                        <li>Smart products</li>
                    </b>
                    Ik heb tijdens mijn studie gewerkt aan apps en java programma’s. Deze apps zijn redelijk smart omdat
                    ze
                    kunnen communiceren met andere programma’s via het internet. Ook zijn ze in staat om gegevens op te
                    slaan en
                    te verzamelen.
                    <b>
                        <li>Servitization</li>
                    </b>
                    Ik heb stage gelopen bij een bedrijf die ‘werkplekken’-software levert aan grote bedrijven. Deze
                    software
                    bracht het gebouw in kaart en hiermee kon je zien welke plekken vrij of gereserveerd waren. Dit
                    gebeurde
                    door middel van het reserveringssysteem dat was gekoppeld aan outlook. Ook onderhoudt en beheert dit
                    bedrijf
                    haar eigen software. Op dit punt was het bedrijf dus best wel ver.
                    <b>
                        <li>Digital Factory</li>
                    </b>
                    Bij mijn stage bedrijf konden klanten ervoor kiezen om zelf een server te draaien of om een
                    server(cloud)
                    van het bedrijf te gebruiken. Hierbij waren dus sommige klanten wel aangesloten en andere klanten
                    niet.
                    Verder heeft een software bedrijf niet echt een ‘factory’ en logistiek nodig voor haar product.
                    Hierdoor
                    heb
                    ik hier nog niet veel ervaring mee.
                    <b>
                        <li>Connected Factories</li>
                    </b>
                    Bij mijn stagebedrijf heb ik wel veel verschillende software gezien die was gekoppeld door middel
                    van
                    APIs.
                    Maar dit is eigenlijk een digitale factory en niet echt een fysieke factory. Hierdoor heb ik ook
                    hiermee
                    niet veel ervaring gehad
                    <b>
                        <li>Sustainable Factory</li>
                    </b>
                    Software is op zichzelf al zeer duurzaam. Ook wordt er nauwelijks materiaal verbruikt bij het maken
                    van
                    software anders dan computers en toebehoren. In een fysieke fabriek zou dit veel meer nodig zijn.
                    <b>
                        <li>Smart Working</li>
                    </b>
                    Smart working past heel erg bij ICT. Technologie wordt ingezet om zware klussen te automatiseren.
                    Bijvoorbeeld om planten water te geven. Een teler hoeft niet meer langs elke plant om deze water te
                    geven
                    maar dit kan allemaal automatisch worden aangestuurd door bijvoorbeeld een systeem of app.
                    <b>
                        <li>Advanced Manufacturing</li>
                    </b>
                    In ICT termen zou dit kunnen zorgen voor eerst ontwerpen en dan produceren. Als je van tevoren een
                    duidelijk
                    ontwerp hebt kan dit veel prototypes schelen. Als je van elke optie eerst een prototype moet maken
                    kost
                    dit
                    veel geld en tijd.
                    <b>
                        <li>Flexible Manufacturing</li>
                    </b>
                    Bij ICT kan dit natuurlijk als geen ander. Van een waterinstallatie naar een programma om robots aan
                    te
                    sturen. Het is allemaal mogelijk. Bij het ene is er meer producten nodig zoals waterslangen etc maar
                    software is zeer flexibel.
                </ul>
